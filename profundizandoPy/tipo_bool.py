#bool contiene valores de True y False
#Tipos numericos, False para 0, True demas valores

valor = 0
resultado = bool(valor)
print(f"Vakir {valor}, resultado {resultado}")
valor = 2
resultado = bool(valor)
print(f"Vakir {valor}, resultado {resultado}")

#tipo colecciones, false para colecciones vacias y verdadero para todas las demas
valor = []
resultado = bool(valor)
print(f"Vakir {valor}, resultado {resultado}")
valor = "Jimena"
resultado = bool(valor)
print(f"Vakir {valor}, resultado {resultado}")
